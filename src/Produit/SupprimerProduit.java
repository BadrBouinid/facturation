    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String ref=jTextField1.getText();
         if (jTextField2.getText().equals("")||jTextField3.getText().equals("")||jTextField4.getText().equals("")||jTextField5.getText().equals("")) {
                JOptionPane.showMessageDialog(null,"Svp veuillez remplir le champ de la recherche");
            }
         else{
        int a=JOptionPane.showConfirmDialog(null,"Voulez-vous vraiment supprimer ce produit ?","delete",JOptionPane.YES_OPTION);
        if (a==0) {
            try{

                Connection con=myCon.getcon();
                Statement stm=con.createStatement();
                stm.executeUpdate("delete from produit where ref='"+jTextField2.getText()+"'");
                JOptionPane.showMessageDialog(null, "La suppression est bien faite");
                setVisible(false);
                new SupprimerProduit().setVisible(true);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e.getMessage());

            }
        }}

    }//GEN-LAST:event_jButton2ActionPerformed

