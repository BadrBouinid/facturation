    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        // TODO add your handling code here:
            
        String ref=jTextField1.getText();
        try {
            Connection con=myCon.getcon();
            Statement stm=con.createStatement();
            ResultSet rs= stm.executeQuery("select * from produit where ref LIKE '%" + ref + "%'");
              if (rs.next()) {
                jTextField2.setText(rs.getString("ref"));
                jTextField3.setText(rs.getString("nom"));
                jTextField4.setText(rs.getString("prix"));
                jTextField5.setText(rs.getString("description"));
            

            }
         
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_jTextField1KeyPressed

