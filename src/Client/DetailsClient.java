    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         com.itextpdf.text.Document doc=new com.itextpdf.text.Document();
        try {
              PdfWriter.getInstance(doc,new FileOutputStream("detail.pdf"));
               doc.open();
            Font fontbold1 = FontFactory.getFont("Times-Roman", 15,Font.UNDERLINE);
            Paragraph title = new Paragraph("Details Client",fontbold1);
            title.setAlignment(Paragraph.ALIGN_CENTER);
            doc.add(title);
             Paragraph line = new Paragraph("\n\n");
             doc.add(line);
             Font fontbold2 = FontFactory.getFont("Times-Roman", 10,Font.BOLD);
              Font fontbold3 = FontFactory.getFont("Times-Roman", 10,Font.NORMAL);
             PdfPTable tbl=new PdfPTable(4);
            //tbl.setHorizontalAlignment(Element.ALIGN_CENTER);
            tbl.addCell(new Phrase("Cin",fontbold2));
            tbl.addCell(new Phrase("Nom",fontbold2));
            tbl.addCell(new Phrase("Tel",fontbold2));
            tbl.addCell(new Phrase("Adresse",fontbold2));
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                String c=jTable1.getValueAt(i, 0).toString();
                String n=jTable1.getValueAt(i, 1).toString();
                String t=jTable1.getValueAt(i, 2).toString();
                String a=jTable1.getValueAt(i, 4).toString();
            
                tbl.addCell(new Phrase(c,fontbold3));
                tbl.addCell(new Phrase(n,fontbold3));
                tbl.addCell(new Phrase(t,fontbold3));
                tbl.addCell(new Phrase(a,fontbold3));
                float[] columnWidths = new float[]{10f, 10f, 20f, 40f};
            tbl.setWidths(columnWidths);
      
            }
            doc.add(tbl);
             doc.close();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

